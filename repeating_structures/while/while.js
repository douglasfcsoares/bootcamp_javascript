// A estrutura de repetição "while" em JavaScript é usada para executar um bloco de código
// repetidamente enquanto uma condição especificada for verdadeira.
// Sua sintaxe básica é:
//
// while (condição) {
//   // código a ser executado enquanto a condição for verdadeira
// }
//
// O loop verifica a condição antes de cada execução, e para quando a condição se torna falsa.
// Isso é útil quando não sabemos exatamente quantas vezes o bloco precisa ser executado.
//
// Exemplo:
// let contador = 0;
// while (contador < 5) {
//   console.log(contador);
//   contador++;
// }

let index = 0;

while (index < 10) {
  console.log(index);
  index++;
}
